using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace LINQ
{
    public class CD
    {
        public CD()
        {

        }

        private string title;

        public string TITLE
        {
            get { return title; }
            set { title = value; }
        }

        private string artist;

        public string ARTIST
        {
            get { return artist; }
            set { artist = value; }
        }

        private string country;

        public string COUNTRY
        {
            get { return country; }
            set { country = value; }
        }

        private string company;

        public string COMPANY
        {
            get { return company; }
            set { company = value; }
        }

        private float price;

        public float PRICE
        {
            get { return price; }
            set { price = value; }
        }

        private int year;

        public int YEAR
        {
            get { return year; }
            set { year = value; }
        }

        private int producer;

        public int PRODUCER
        {
            get { return producer; }
            set { producer = value; }
        }

       

    }
}
